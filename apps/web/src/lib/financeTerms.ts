/**
 * financeTerms.ts — единый глоссарий финансовых терминов для UI.
 * Используется на /finance/*, /bookings/[id], StatusCell.
 *
 * Правила унификации (T12):
 *   «Приход» (не «Доход», не «Выручка») — для поступлений
 *   «Расход» — для трат
 *   «Оплачено» (не «Получено») — сумма фактически внесённых платежей
 *   «К получению» — сумма оставшегося долга (aggregate)
 *   «Просрочено» — когда дата платежа уже прошла
 *   «Частично оплачено» — есть платежи, но ещё не всё
 *   «Аннулирован» — статус аннулированного платежа
 *   «Черновик» — booking/payment в статусе DRAFT
 */
export const FINANCE_TERMS = {
  income: "Приход",
  expense: "Расход",
  outstanding: "К получению",
  paid: "Оплачено",
  overdue: "Просрочено",
  partial: "Частично оплачено",
  void: "Аннулирован",
  draft: "Черновик",
  billed: "Выставлено",
  received: "Принято",
  total: "Итог",
  paymentDate: "Дата платежа",
  notPaid: "Не оплачено",
} as const;

export type FinanceTerm = keyof typeof FINANCE_TERMS;
