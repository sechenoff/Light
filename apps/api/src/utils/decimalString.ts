import { z } from "zod";

/**
 * Zod-схема для денежных значений. Принимает строку или число, обрезает пробелы,
 * требует формат простого десятичного числа, ограничивает до 1 миллиарда.
 * Генерирует чистую ошибку 400 через Zod вместо 500 от DecimalError.
 */
export const decimalString = z
  .union([z.string(), z.number()])
  .transform((v) => (typeof v === "number" ? String(v) : v.trim()))
  .refine((s) => /^\d+(\.\d+)?$/.test(s), {
    message: "Некорректная сумма — используйте число без пробелов и запятых",
  })
  .refine((s) => Number(s) <= 1_000_000_000, {
    message: "Сумма превышает допустимый лимит (1 000 000 000)",
  });
